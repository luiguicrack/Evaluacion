    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
        int total = 0, perro, cant, tot = 0, hor;
        hor = Integer.parseInt(Hora.getText());
        cant = Integer.parseInt(Cantidad.getText());
        for (int i = 1; i<= cant; i++){
            perro = Integer.parseInt(JOptionPane.showInputDialog("""
                                                                 1. PERRO GRANDE
                                                                 2. PERRO MEDIANO
                                                                 3. PERRO PEQUEÃ‘O
                                                                 
                                                                 DIGITE EL PERRO #"""+i));
            if (perro == 1){
                total += 10000*hor;
            } else if (perro == 2){
                total += 5000*hor;
            } else if (perro == 3){
                total += 3000*hor;
            } else {
                JOptionPane.showMessageDialog(null, "Opcion incorrecta");
            }
            tot = total;
        }
        if (cant>1){
            total *= 0.10;
            tot  = tot - total;
        } else{
            tot = total;
        }
        Total.setText(tot+"");
    }//GEN-LAST:event_CalcularActionPerformed

